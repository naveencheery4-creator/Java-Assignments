public class EqualNotEqualInLoops {

    public static void main(String[] args) {

        int a = 10;
        int b = 20;

        System.out.println("a = " + a);
        System.out.println("b = " + b);

        System.out.println("Equal (==): " + (a == b));
        System.out.println("Not Equal (!=): " + (a != b));
    }
}

/* output
  a = 10,
b = 20,
 false,
  true
  */
